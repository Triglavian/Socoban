using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

class Box : Movable
{
	public bool m_IsFilled;
	public Box()
	{
		m_X = 0;
		m_Y = 0;
		m_IsFilled = false;
	}
	public Box(int p_X, int p_Y)
	{
		m_X = p_X;
		m_Y = p_Y;
		m_IsFilled = false;
	}
	public bool GetStatus() //get hole's status
	{
		return m_IsFilled;
	}
}
